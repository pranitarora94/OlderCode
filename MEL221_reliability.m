function [r,req] = MEL221_reliability(X)
%X is a 9 element vector, each of which takes values as {0,1,2}

R1=0.90 + 0.04*X(1)-0.01*(X(1)^2);                       %opt1: 0.93, opt2:0.94
R2=0.93 + 0.05*X(2)-0.01*(X(2)^2);                       %opt1: 0.97, opt2:0.99
R3=0.92 + 0.07*X(3)-0.02*(X(3)^2);                       %opt1: 0.97, opt2:0.98
R4=0.92 + 0.10*X(4)-0.04*(X(4)^2);                       %opt1: 0.98, opt2:0.96
R5=0.88 + 0.13*X(5)-0.04*(X(5)^2);                       %opt1: 0.97, opt2:0.98
R6=0.94 + 0.03*X(6)-0.01*(X(6)^2);                       %opt1: 0.96, opt2:0.96
R7=0.93 + 0.05*X(7)-0.02*(X(7)^2);                       %opt1: 0.96, opt2:0.99
R8=0.93 + 0.04*X(8)-0.02*(X(8)^2);                       %opt1: 0.95, opt2:.96
R9=0.95 + 0.04*X(9)-0.03*(X(9)^2);                       %opt1: 0.96, opt2:0.91

Rn1=R1*R2*R3;
Rn2=R4*R5*R6;
Rn3=R7*R8*R9;
Rnet=(Rn2*Rn3) + Rn1*Rn2*(1-Rn3) + Rn1*(1-Rn2)*Rn3;

r= [0.95-Rnet];

req=[];

end
